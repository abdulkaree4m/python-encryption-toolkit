# الدالة الثانية: railway_de(text, key)
# ------------------------------------------------
# هذه الدالة مصممة لفك التشفير، لكنها لا تعمل بنفس منطق التشفير 100% (قد تعطي نتيجة خاطئة).
#
# - المدخلات:
#   text: النص المشفر
#   key: عدد الأسطر المستخدم عند التشفير
#
# - الخطوات:
#   1) نحسب عدد الأعمدة col = طول النص ÷ عدد الأسطر (مع تقريب للأعلى).
#   2) ننشئ مصفوفة (قائمة قوائم) بعدد أسطر = key، وعدد أعمدة = col.
#   3) نوزع النص المشفر في المصفوفة (سطر بسطر).
#   4) نقرأ المصفوفة عمود بعمود ونجمع الحروف لنرجع النص.
#
# ملاحظة: هذه الطريقة تشبه التشفير بالأعمدة (Columnar Cipher)
#         لذلك قد لا تعطي النص الأصلي الصحيح تماماً.

