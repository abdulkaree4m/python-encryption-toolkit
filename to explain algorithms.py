# الدالة الأولى: keyless(plaintext, key)
# ------------------------------------------------
# هذه الدالة تنفذ خوارزمية (Rail Fence Cipher) للتشفير.
#
# - المدخلات:
#   plaintext: النص الأصلي المراد تشفيره
#   key: عدد الأسطر (السكك) المستخدم في التشفير
#
# - الخطوات:
#   1) تنشئ قائمة l تحتوي على عدد عناصر = key، وكل عنصر عبارة عن نص فارغ.
#      -> هذه القائمة تمثل الأسطر التي سنوزع فيها الحروف.
#   2) تمر على كل حرف من النص (plaintext) وتوزعه على الأسطر بطريقة متعرجة (zig-zag).
#      - المتغير row يحدد أي سطر نحن فيه حالياً.
#      - المتغير d يحدد الاتجاه (هل ننزل لأسفل أم نصعد لأعلى).
#      - إذا وصلنا لأول أو آخر سطر، نغير الاتجاه.
#   3) بعد توزيع الحروف، نجمع كل النصوص الموجودة في القائمة l ونرجعها كنص مشفر.
#
# مثال:
#   plaintext = "HELLO", key = 3
#
#   الشكل:
#   H   L
#   E L O
#
#   النتيجة = "HLELO"

